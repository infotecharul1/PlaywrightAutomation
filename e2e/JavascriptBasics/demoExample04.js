let m = 10;
m = m + 5; // Increment
console.log(m); // Outputs: 15
m += 5; // Increment by 5
console.log(m); // Outputs: 20

m *= 2; // Multiply by 2
console.log(m); // Outputs: 40

m /= 4; // Divide by 4 (Quotient for the operation)
console.log(m); // Outputs: 10

m %= 3; // Modulus by 3 (Remainder for the operation)
console.log(m); // Outputs: 1
